package javaexamples;

public class Elseif {
public static void main(String args[])
{
	int a=40;
	if(a==10)
{
		System.out.println("a value is 10");
		}

	else if(a==20)
	{
		System.out.println("a value is 20");
	}
	else	if(a==30)
	{
		System.out.println("a value is 30");
	}
	else {
		System.out.println("else stmt executed");
	}
}
}
